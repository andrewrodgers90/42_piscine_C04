#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	arr[10];
	long	num;
	int	i;

	num = nb;
	i = 0;
	if (num == 0)
		write(1, "0", 1);
	if (num < 0)
	{
		write(1, "-", 1);
		num *= -1;
	}
	while (num > 0)
	{
		arr[i++] = (num % 10) + 48;
		num /= 10;
	}
	while (i > 0)
		write(1, &arr[--i], 1);
}
